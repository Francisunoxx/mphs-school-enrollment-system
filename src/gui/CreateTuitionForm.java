    private void addTuitionFeeJbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addTuitionFeeJbtnActionPerformed
        if (basicFeeAmountJtf.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill out Amount fee field");
        } else {
            DefaultTableModel myModel = (DefaultTableModel) feesJtbl.getModel();
            double myBasicFeeAmount = Double.parseDouble(basicFeeAmountJtf.getText());
            DecimalFormat myDf = new DecimalFormat("#,###.00");
            myModel.addRow(new Object[]{"Basic Fee", '\u20B1' + " " + myDf.format(myBasicFeeAmount), "Tuition"});

            double totalOfTuition = 0;
            double doubleAmount;

            totalOfFees = 0;
            for (int x = 0; x < feesJtbl.getRowCount(); x++) {
                String valueWithPesosSign = ((String) myModel.getValueAt(x, 1));
                String stringAmount = valueWithPesosSign.replaceAll("\\P{Print}", "").replaceAll(",","");
                doubleAmount = Double.valueOf(stringAmount);
                totalOfTuition += doubleAmount;
            }
            totalOfFees = totalOfTuition;
            totalJlbl.setText(""+'\u20B1' +" "+ myDf.format(totalOfFees) );
            //JOptionPane.showMessageDialog(null,"Total: "+ totalOfFees);
            basicFeeAmountJtf.setText("");
        }
    }//GEN-LAST:event_addTuitionFeeJbtnActionPerformed

