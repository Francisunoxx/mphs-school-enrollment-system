    private void removeSelectedJbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeSelectedJbtnActionPerformed

        if(feesJtbl.getSelectedRowCount() > 0){
            int mySelectedRow = feesJtbl.getSelectedRow();
            String firstColumnValue = (String) feesJtbl.getValueAt(mySelectedRow, 0);
            String secondColumnValue = (String) feesJtbl.getValueAt(mySelectedRow, 1);
            String thirdColumnValue = (String) feesJtbl.getValueAt(mySelectedRow, 2);

            int choice = JOptionPane.showConfirmDialog(null, "Remove?\n " + firstColumnValue + "\n" + secondColumnValue + "\n" + thirdColumnValue, "Remove", JOptionPane.YES_NO_OPTION);
            if (choice == JOptionPane.YES_OPTION) {
                DefaultTableModel myModel = (DefaultTableModel) feesJtbl.getModel();
                myModel.removeRow(mySelectedRow);
                secondColumnValue = secondColumnValue.replaceAll("\\P{Print}", "").replaceAll(",","");
                totalOfFees = (totalOfFees - Double.parseDouble(secondColumnValue) );
                DecimalFormat myDf = new DecimalFormat("#,###.00");
                totalJlbl.setText(""+'\u20B1' +" "+ myDf.format(totalOfFees) );
            }
        }else if(feesJtbl.getSelectedRowCount() == 0){
            JOptionPane.showMessageDialog(null,"Please select the row you want to delete");
        }
    }//GEN-LAST:event_removeSelectedJbtnActionPerformed


